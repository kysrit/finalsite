function myFunction(imgs) {
    var x = 600;
    w = window.innerWidth;
    if(x<w){
    var bigimg = document.getElementById("largeImg");
    bigimg.src = imgs.src;
    bigimg.parentElement.style.display = "block";
    }
  }
